    public void avanzarFlujoVenta(int paso) {
    switch (paso) {
        case 1:
            escritorio.repaint();
            VistaClienteProyecciones vcp = new VistaClienteProyecciones(this);
            vcp.setVisible(true);
            escritorio.add(vcp);
            escritorio.moveToFront(vcp);
            break;
        case 2:
            escritorio.repaint();
            VistaClienteElegirAsiento lav = new VistaClienteElegirAsiento(this);
            lav.setVisible(true);
            escritorio.add(lav);
            escritorio.moveToFront(lav);
            break;
        case 3:
            escritorio.repaint();
            VistaClientePagar vcpg = new VistaClientePagar(this);
            vcpg.setVisible(true);
            escritorio.add(vcpg);
            escritorio.moveToFront(vcpg);
            
            break;
        case 4:
            escritorio.repaint();
            VistaClienteImprimirEntrada vcie = new VistaClienteImprimirEntrada(this);
            vcie.setVisible(true);
            escritorio.add(vcie);
            escritorio.moveToFront(vcie);
            
            break;
        // ...
    }
}

